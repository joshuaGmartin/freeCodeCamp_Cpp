#include <iostream>

int main()
{
    // ==============================================================
    // +
    // ==============================================================
    std::cout << "========================== + ==========================" << std::endl;
    int value{45};

    std::cout << "value: " << value << std::endl;

    value += 5; // same as value = value + 5
    std::cout << "value += 5: " << value << std::endl;

    // ==============================================================
    // -
    // ==============================================================
    std::cout << "========================== - ==========================" << std::endl;

    std::cout << "value: " << value << std::endl;

    value -= 5; // same as value = value - 5
    std::cout << "value -= 5: " << value << std::endl;

    // ==============================================================
    // *
    // ==============================================================
    std::cout << "========================== * ==========================" << std::endl;

    std::cout << "value: " << value << std::endl;

    value *= 2; // same as value = value * 2
    std::cout << "value *= 2: " << value << std::endl;

    // ==============================================================
    // /
    // ==============================================================
    std::cout << "========================== / ==========================" << std::endl;

    std::cout << "value: " << value << std::endl;

    value /= 3; // same as value = value / 3
    std::cout << "value /= 3: " << value << std::endl;

    // ==============================================================
    // %
    // ==============================================================
    std::cout << "========================== % ==========================" << std::endl;

    std::cout << "value: " << value << std::endl;

    value %= 11;
    std::cout << "value %= 11: " << value << std::endl;

    return 0;
}
